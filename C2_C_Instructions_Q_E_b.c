#include <stdio.h>
int main()
{
    int a,b,c,d;
    a=2%5;
    b=-2%5;
    c=2%-5;
    d=-2%-5;
    //  a%b if (a<b) Then Allays Answer is a.
    printf("a=%d\nb=%d\nc=%d\nd=%d",a,b,c,d);
    return 0;
}

