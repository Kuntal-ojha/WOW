#include <stdio.h>
#include <math.h>
int main()
{
    float a=0.5;
    float w,x,y,z;
    w= sin(a);
    x= cos(a);
    y= tan(a);
    z= pow(a,2);
    printf("Sin(%f)=%f\nCos(%f)=%f\nTen(%f)=%f\nPow(%f)=%f",a,w,a,x,a,y,a,z);
    return 0;
}

